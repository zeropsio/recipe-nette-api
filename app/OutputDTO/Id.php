<?php

declare(strict_types=1);

namespace App\OutputDTO;

class Id
{
    /** @var int */
    public $id;

    public function __construct(int $id)
    {
        $this->id = $id;
    }

    public function getId(): int
    {
        return $this->id;
    }

    public function setId(int $id): self
    {
        $this->id = $id;
        return $this;
    }
}
